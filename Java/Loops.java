public class Loops {
    public static void main(String[] args) {
        // for loop
        String myData = "this is a data";
        for (int i =0; i < 10; i++){
        System.out.println(myData);
        }
        // while loop
        {
            int i = 0;
        while (i < 5) {
        System.out.println(myData);
        i++;
        }
        }
        
        // do while loop
        {int i = 0;
            do {
            System.out.println(i);
            i++;
            }
            while (i < 5);}
    }
}
